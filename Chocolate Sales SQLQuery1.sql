    store_name,
    city
FROM stores

-----Q5: Products with cocoa percentage greater than 70%.
SELECT
    product_name,
    cocoa_percent
FROM products
WHERE cocoa_percent > 70

-----Q6: How many products does each brand offer?
SELECT
    brand,
    COUNT(*) AS Product_Count
FROM products
GROUP BY brand
ORDER BY Product_Count DESC

